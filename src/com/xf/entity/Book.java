package com.xf.entity;

public class Book {
  public int id;
  public String name;
  public double price;
  public String image;
  public int stock;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public double getPrice() {
	return price;
}
public void setPrice(double price) {
	this.price = price;
}
public String getImage() {
	return image;
}
public void setImage(String image) {
	this.image = image;
}
public int getStock() {
	return stock;
}
public void setStock(int stock) {
	this.stock = stock;
}
  
}
